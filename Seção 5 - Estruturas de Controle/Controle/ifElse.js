const imprimirResultado = function(nota) {
    if (nota >= 7) {
        console.log('Aprovado!')
    } else {
        console.log('Reprovado!')
    }
}

imprimirResultado(10)
imprimirResultado(4)
imprimirResultado('Epa') // Cuidado!!, em vista que JavaScript é fracamente tipado, assim deixando comparar uma string quando a equação é matemática.